class MenuItemArguments {
  final bool darkMode;
  final String name;
  final int price;
  final double rating;
  final int calories;
  final String image;

  MenuItemArguments({
    required this.darkMode,
    required this.name,
    required this.price,
    required this.rating,
    required this.calories,
    required this.image,
  });
}
